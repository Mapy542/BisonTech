int x = 5; // create new integer variable and set it to five.

int x[5] = {1, 2, 3, 4, 5}; // create a new integer list, array, and assign the
                            // five values to 1,2,3,4,5.

x = x + 3;
x += 3; // both add three to variable. works with + - * / %

x / 3; // returns x divided by three to the whole number. if x = 5 then 5/3
       // = 1.6666 so x = 1

x % 3; // returns the remained from long division. if x = 13 then 13 / 3 = 4.333
       // or 4 remainder 1. Mod returns the 1.
